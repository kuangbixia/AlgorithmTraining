int floodFill(vector<vector<int> > map, int n, int m) {
    // write code here
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            if (map[i][j] == 1) {
                map[i][j] = -1;
            }
        }
    }
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            if ((i==0&&j==0)||(map[i][j] != -1&&map[i][j]!=0)) {
                int next = map[i][j] + 1;
                if (i != 0) {
                    if (map[i - 1][j] == 0 || map[i - 1][j] > next) {
                        map[i - 1][j] = next;
                    }
                }
                if (i != (n - 1)) {
                    if (map[i + 1][j] == 0 || map[i + 1][j] > next) {
                        map[i + 1][j] = next;
                    }
                }
                if (j != 0) {
                    if (map[i][j - 1] == 0 || map[i][j - 1] > next) {
                        map[i][j - 1] = next;
                    }
                }
                if (j != (m - 1)) {
                    if (map[i][j + 1] == 0 || map[i][j + 1] > next) {
                        map[i][j + 1] = next;
                    }
                }
            }
        }
    }
    return map[n - 1][m - 1];
}

